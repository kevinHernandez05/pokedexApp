package kev.practice.pokedex.model;

public class Pokemon {

    private String name;
    private String url;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        url = url;
    }
}
